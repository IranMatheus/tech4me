function listaConcat() {
    const verao = ["Janeiro", "Fevereiro", "Março"];
    const inverno = ["Julho", "Agosto", "Setembro"];

    const climas = verao.concat(inverno);

    console.log("verão: " + verao);
    console.log("inverno: " + inverno);
    console.log("climas:" + climas);

}

listaConcat();